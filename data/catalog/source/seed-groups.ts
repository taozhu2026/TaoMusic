const buildSeedTracksFromGroup = (group: CatalogSeedGroup): CatalogSeedTrack[] => {
  return group.artists.flatMap((artist, artistIndex) =>
    group.titles.map((title, titleIndex) => ({
      seedId: `${group.slug}-${artistIndex + 1}-${titleIndex + 1}`,
      groupSlug: group.slug,
      title,
      artist,
      album: group.albums[(artistIndex + titleIndex) % group.albums.length],
      language: group.language,
      region: group.region,
      rawTags: group.rawTags,
      descriptorHints: group.descriptorHints,
      lyricalThemeHints: group.lyricalThemeHints,
      instrumentationHints: group.instrumentationHints,
      artworkColorHint: group.artworkColors[(artistIndex + titleIndex) % group.artworkColors.length],
      year: group.yearStart + ((artistIndex + titleIndex) % 8),
      audioProfile: group.audioProfile,
      searchKeywords: group.searchKeywords,
    })),
  );
};

export const CATALOG_SEED_TRACKS: CatalogSeedTrack[] = CATALOG_SEED_GROUPS.flatMap(
  buildSeedTracksFromGroup,
);
